import { useOCAuth } from "@opencampus/ocid-connect-js";

export const useLogin = () => {
  const { ocAuth } = useOCAuth();

  const handleLogin = async () => {
    try {
      await ocAuth.signInWithRedirect({ state: "opencampus" });
    } catch (error) {
      console.error("Login error:", error);
    }
  };

  return handleLogin;
};
